"""8/10/16-bit integer bit-depth parity tests.

For every filter that accepts more than one integer bit depth, run it on the same
content at each depth, normalize the output to [0,1] by the format peak, and
assert the results match within the precision of the lower depth. This surfaces
bit-depth-specific bugs (a hard-coded 255 peak, overflow at 16-bit, scaling that
only holds at one depth, ...).

Not covered: single-int-depth filters (Checkmate, CombMask, CombMaskMT, ColorMap,
AdaptiveBinarize). PackRGB is excluded -- its output is a depth-specific bit
packing (8+8+8 vs 10+10+10 into one Gray32), so cross-depth values are not
comparable. CLAHE is excluded too (histogram equalisation is depth-resolution
dependent by design; see its section below).
"""

import pytest
import vapoursynth as vs

from helpers import max_abs_diff, props


def peak(bits: int) -> int:
    return (1 << bits) - 1


# (bits, format) sets per colour family. Use the subset a filter actually accepts.
GRAY = [(8, vs.GRAY8), (10, vs.GRAY10), (16, vs.GRAY16)]
YUV420 = [(8, vs.YUV420P8), (10, vs.YUV420P10), (16, vs.YUV420P16)]
YUV444 = [(8, vs.YUV444P8), (10, vs.YUV444P10), (16, vs.YUV444P16)]
RGB = [(8, vs.RGB24), (10, vs.RGB30), (16, vs.RGB48)]


def normalize(clip: vs.VideoNode, bits: int) -> vs.VideoNode:
    """Integer clip -> f32 in [0,1] (divide every plane by the format peak).
    Lets outputs at different bit depths be compared on a common scale."""
    out = clip.format.replace(sample_type=vs.FLOAT, bits_per_sample=32).id
    return clip.std.Expr(f"x {peak(bits)} /", format=out)


def assert_pixel_parity(results, *, lsb: float = 4.0, n: int = 0) -> None:
    """results: list of (bits, clip). Normalize each output to [0,1] and compare
    every lower-depth result to the highest-depth one. Tolerance is `lsb` LSBs of
    the *lower* depth (its inherent quantization), so a correct filter passes and
    a scaling/overflow bug (diff far larger than a few LSB) fails."""
    results = sorted(results, key=lambda r: -r[0])
    hi_bits, hi_clip = results[0]
    refn = normalize(hi_clip, hi_bits)
    for bits, clip in results[1:]:
        cn = normalize(clip, bits)
        tol = lsb / peak(bits)
        for p in range(refn.format.num_planes):
            d = max_abs_diff(refn, cn, plane=p, n=n)
            assert d <= tol, f"{bits}-bit vs {hi_bits}-bit, plane {p}: max|Δnorm| = {d} > {tol} ({lsb} LSB)"


def assert_prop_parity(results, keys, *, tol: float, n: int = 0) -> None:
    """results: list of (bits, clip). Compare frame-prop values across depths.
    Only valid for depth-independent props (already normalized, or a dB/score);
    for raw per-depth props normalize with peak(bits) in the test instead."""
    results = sorted(results, key=lambda r: -r[0])
    hi_bits, hi_clip = results[0]
    ref = props(hi_clip, n)
    for bits, clip in results[1:]:
        cur = props(clip, n)
        for k in keys:
            assert abs(ref[k] - cur[k]) <= tol, f"{k}: {bits}-bit={cur[k]} {hi_bits}-bit={ref[k]} (tol {tol})"


