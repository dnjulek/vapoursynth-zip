"""f16 <-> f32 parity tests.

For every filter that accepts a half-float (f16) format, run it on f16 input and
on the *byte-identical* f32 input (the f16 data widened to f32), then assert the
two outputs match to within f16 output precision.

Using identical data in both containers isolates the filter's f16 code path from
input quantization: any difference beyond f16 output rounding means the f16 path
diverges from the f32 path.

Filters that reject f16 outright (Checkmate, CombMask(MT), CLAHE, ColorMap,
Deband, PackRGB, AdaptiveBinarize, XPSNR, SSIMULACRA2, ...) are covered by their
own validation tests.
"""

import pytest
import vapoursynth as vs

from helpers import max_abs_diff, props

# f16 format -> its matching f32 format
F16_TO_F32 = {
    vs.GRAYH: vs.GRAYS,
    vs.YUV420PH: vs.YUV420PS,
    vs.YUV422PH: vs.YUV422PS,
    vs.YUV444PH: vs.YUV444PS,
    vs.RGBH: vs.RGBS,
}


def pair(make, f16_fmt: int):
    """(f16 clip, f32 clip) carrying byte-identical data: build the f16 clip,
    then widen a copy to f32 (Point = format change only, no resampling) so any
    output difference is the filter's f16 path, not input quantization."""
    f16 = make(f16_fmt)
    f32 = f16.resize.Point(format=F16_TO_F32[f16_fmt])
    return f16, f32


def assert_pixel_parity(out_f16: vs.VideoNode, out_f32: vs.VideoNode, *, tol: float, n: int = 0) -> None:
    """Widen the f16 output to f32 and assert max|f16-f32| <= tol per plane."""
    widened = out_f16.resize.Point(format=out_f32.format.id)
    for p in range(out_f32.format.num_planes):
        d = max_abs_diff(widened, out_f32, plane=p, n=n)
        assert d <= tol, f"plane {p}: max|f16-f32| = {d} > {tol}"


def assert_prop_parity(out_f16: vs.VideoNode, out_f32: vs.VideoNode, keys, *, tol: float, n: int = 0) -> None:
    """Assert abs(prop_f16 - prop_f32) <= tol for each requested frame-prop key."""
    a, b = props(out_f16, n), props(out_f32, n)
    for k in keys:
        assert abs(a[k] - b[k]) <= tol, f"{k}: f16={a[k]} f32={b[k]} (tol {tol})"


